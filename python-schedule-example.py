def стартирай_планировчика():
    """Тази функция ще се изпълнява в отделна нишка и ще проверява за планирани задачи."""
    while True:
        schedule.run_pending()
        time.sleep(1)

# Планирайте задачата да се изпълни всеки ден в 15:30 часа
schedule.every().day.at("15:30").do(задача)

# Можете да планирате и други задачи
schedule.every().monday.at("09:00").do(задача)

# Създайте и стартирайте нишка за планировчика
planner_thread = threading.Thread(target=стартирай_планировчика)
planner_thread.start()

# Основната програма може да продължи да работи тук
